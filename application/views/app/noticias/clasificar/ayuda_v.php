<h3 class="text-main text-center">¿Cómo hacerlo?</h3>
<h4 class="">1. Lee la noticia</h4>
<ul>
    <li>Lee el titular y el texto inicial de la noticia.</li>
    <li>Con esta información es muy posible que puedas hacer la clasificación.</li>
    <li>
        Si necesitas más información haz clic en "Ver más..." para abrir en otra pestaña 
        la publicación de la noticia.
    </li>
</ul>
<h4 class="">2. ¿Es Negativa/Neutral/Positiva?</h4>
<p>Establece si la noticia te produce una emoción positiva, negativa o neutral.</p>
<h4 class="">3. Asígnale un Tema </h4>
<p>
    Establece el tema principal sobre el cual trata la noticia. Si el tema
    correspondiente no coincide con alguno de los disponibles elige "Otro".
</p>
<h4 class="">4. ¿La compartirías?</h4>
<p>
    Piensa si es el tipo de noticia que tú realmente compartirías en las redes sociales.
</p>
<h4 class="">Ten en cuenta</h4>
<ul>
    <li>La clasificación se puede guardar cuando las tres categorías hayan sido asignadas.</li>
    <li>Si con la información disponible no se puede hacer una clasificación válida, haz clic en [SALTAR].</li>
</ul>